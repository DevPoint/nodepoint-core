<?php

namespace NodePoint\Core\Library;

class MagicFieldCallInfo {

	/*
	 * @var string
	 */
	public $field;	

	/*
	 * @var string
	 */
	public $functionCall;

	/*
	 * @var boolean
	 */
	public $autoGenerated;

	/*
	 * @param $field string
	 * @param $functionCall string
	 * @param $options array
	 */
	public function __construct($field, $functionCall, $options=array())
	{
		$this->field = $field;
		$this->functionCall = $functionCall;
		$this->autoGenerated = (!empty($options['auto']));
	}
}
